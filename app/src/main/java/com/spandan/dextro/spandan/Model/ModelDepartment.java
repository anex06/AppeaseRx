package com.spandan.dextro.spandan.Model;

public class ModelDepartment {
    private String deptId;
    private String deptName;
    private String deptBanner;
    private String deptImage;

    public String getDeptId() {
        return deptId;
    }

    public void setDeptId(String deptId) {
        this.deptId = deptId;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getDeptBanner() {
        return deptBanner;
    }

    public void setDeptBanner(String deptBanner) {
        this.deptBanner = deptBanner;
    }

    public String getDeptImage() {
        return deptImage;
    }

    public void setDeptImage(String deptImage) {
        this.deptImage = deptImage;
    }
}
